const useSeatsStore = create((set) => ({
  seats: seatsData,
  
  updateSeatStatus: (section, row, col, newStatus) =>
    set((state) => ({
      seats: state.seats.map((seat) =>
        seat.section === section && seat.row === row && seat.col === col
          ? { ...seat, status: newStatus }
          : seat
      ),
    })),

  // Функция для обновления позиции стула
  updateSeatPosition: (section, row, col, newTop, newLeft) =>
    set((state) => ({
      seats: state.seats.map((seat) =>
        seat.section === section && seat.row === row && seat.col === col
          ? { ...seat, top: newTop, left: newLeft }
          : seat
      ),
    })),
}));

export default useSeatsStore;

